package com.hk.app;

public class Score {
	private String name;
	private int kor;
	private int chn;
	private int jpn;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getKor() {
		return kor;
	}
	public void setKor(int kor) {
		this.kor = kor;
	}
	public int getChn() {
		return chn;
	}
	public void setChn(int chn) {
		this.chn = chn;
	}
	public int getJpn() {
		return jpn;
	}
	public void setJpn(int jpn) {
		this.jpn = jpn;
	}

}
