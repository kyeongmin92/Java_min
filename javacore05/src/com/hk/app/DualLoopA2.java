package com.hk.app;

public class DualLoopA2 {

	public static void main(String[] args) {
		// * * * * * 반복문으로 출력
		String star = "* ";
      for(int j=0; j<5; j++) { 
    	  
    	for(int i=0; i<5; i++) { 
    		System.out.print(star);    		
		}
    	System.out.println(" ");
      }
      
	}

}
